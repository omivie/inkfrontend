#!/usr/bin/env node
/**
 * audit-merchant-center-readiness.mjs
 * ===================================
 * Google Merchant Center readiness auditor for InkCartridges.co.nz.
 *
 * Two passes:
 *
 *   1. STATIC pass (always runs, deterministic, CI-friendly) — audits the
 *      files in this repo for the trust/consistency issues Merchant Center
 *      flags: inconsistent payment-method advertising, legacy/leaked emails,
 *      the dispatch cut-off, a public Admin link in page source, and whether
 *      the /genuine-vs-compatible page exists and is wired up. A failure here
 *      exits non-zero.
 *
 *   2. LIVE pass (best-effort; needs network) — fetches the production Google
 *      Shopping feed and a sample of the product API and flags per-SKU data
 *      that would trip "Misrepresentation": compatible items titled like OEM,
 *      OEM identifiers on third-party products, availability/price/condition
 *      gaps, and corrupted compatibility / yield text ("5%", "PAGES",
 *      "Brother Brother", impossible yields). The live pass NEVER fails the
 *      build (the feed is generated by the backend repo, not here) — it prints
 *      an actionable, SKU-level report for backend / admin remediation.
 *
 * Usage:
 *   node scripts/audit-merchant-center-readiness.mjs            # static + live
 *   node scripts/audit-merchant-center-readiness.mjs --static   # static only
 *   node scripts/audit-merchant-center-readiness.mjs --json     # machine output
 *
 * Exit code: 0 when the static pass is clean, 1 otherwise.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const SITE = path.join(ROOT, 'inkcartridges');

const ARGS = new Set(process.argv.slice(2));
const STATIC_ONLY = ARGS.has('--static');
const JSON_OUT = ARGS.has('--json');
const FEED_URL = 'https://inkcartridges.co.nz/feeds/google-shopping.xml';
const API_URL = 'https://api.inkcartridges.co.nz/api/products?limit=200';

// The single source of truth for what the site is allowed to advertise.
const CANONICAL_PAYMENTS = ['Visa', 'Mastercard', 'American Express', 'PayPal', 'Apple Pay', 'Google Pay', 'Klarna'];
const OEM_BRANDS = ['Brother', 'Canon', 'Epson', 'HP', 'Hewlett', 'Kyocera', 'Lexmark', 'OKI', 'Samsung', 'Fuji Xerox', 'Xerox', 'Ricoh', 'Dell'];
const OEM_TERMS = /\b(genuine|original|oem|manufacturer warranty|authentic)\b/i;
const COMPATIBLE_LEADS = /^(compatible|third[-\s]?party|generic|remanufactured|refilled)\b/i;
const MIN_PLAUSIBLE_YIELD = 15;
const MAX_PLAUSIBLE_YIELD = 60000; // above this for a low-priced consumable is suspicious

const findings = [];   // static failures (fail the build)
const notes = [];      // informational (live pass, or non-blocking)

function fail(area, msg) { findings.push({ area, msg }); }
function note(area, msg) { notes.push({ area, msg }); }

function read(p) {
  try { return fs.readFileSync(p, 'utf8'); } catch { return null; }
}

function walk(dir, ext, out = []) {
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, e.name);
    if (e.isDirectory()) {
      if (e.name === 'node_modules') continue;
      walk(p, ext, out);
    } else if (e.name.endsWith(ext)) {
      out.push(p);
    }
  }
  return out;
}

// ──────────────────────────────────────────────────────────────────────────
// STATIC PASS
// ──────────────────────────────────────────────────────────────────────────

function auditPaymentConsistency() {
  const legal = read(path.join(SITE, 'js', 'legal-config.js')) || '';
  const m = legal.match(/paymentMethods:\s*\[([^\]]*)\]/);
  if (!m) { fail('payments', 'legal-config.js paymentMethods array not found'); return; }
  const listed = m[1].split(',').map(s => s.replace(/['"\s]/g, '')).filter(Boolean)
    .map(s => s.replace(/([a-z])([A-Z])/g, '$1 $2'));
  const norm = (a) => a.map(x => x.toLowerCase().replace(/[^a-z]/g, '')).sort();
  if (JSON.stringify(norm(listed)) !== JSON.stringify(norm(CANONICAL_PAYMENTS))) {
    fail('payments', `legal-config paymentMethods ${JSON.stringify(listed)} != canonical ${JSON.stringify(CANONICAL_PAYMENTS)}`);
  }

  // Badge SVGs / spans must cover exactly the canonical set in footer + cart + 404.
  const surfaces = {
    'js/footer.js': read(path.join(SITE, 'js', 'footer.js')) || '',
    'html/cart.html': read(path.join(SITE, 'html', 'cart.html')) || '',
    '404.html': read(path.join(SITE, '404.html')) || '',
  };
  const aliases = { amex: 'American Express', 'american express': 'American Express' };
  for (const [file, src] of Object.entries(surfaces)) {
    const found = new Set();
    for (const method of CANONICAL_PAYMENTS) {
      const re = new RegExp(`aria-label="${method}"|title="${method}"|>${method}<`, 'i');
      if (re.test(src)) found.add(method);
    }
    // Amex is rendered as "Amex" text on the plain 404 badge row.
    if (/aria-label="American Express"|title="American Express"|>Amex</i.test(src)) found.add('American Express');
    const missing = CANONICAL_PAYMENTS.filter(x => !found.has(x));
    // Klarna, Apple/Google Pay are optional on the reduced 404 row historically;
    // now we require the full set everywhere for consistency.
    if (missing.length) {
      fail('payments', `${file} is missing payment badges: ${missing.join(', ')}`);
    }
    // Guard against a re-introduced BNPL we do not offer.
    if (/afterpay|laybuy|zip pay/i.test(src)) {
      fail('payments', `${file} advertises a BNPL method not in the canonical set (Afterpay/Laybuy/Zip)`);
    }
  }
}

function auditEmailsAndClaims() {
  const files = [...walk(SITE, '.html'), ...walk(path.join(SITE, 'js'), '.js'), ...walk(path.join(SITE, 'css'), '.css')];
  for (const f of files) {
    const src = read(f);
    if (!src) continue;
    const rel = path.relative(ROOT, f);
    if (/windowslive|inkandtoner@/i.test(src)) {
      fail('identity', `${rel} contains a legacy email (windowslive / inkandtoner@)`);
    }
    // Personal owner Gmail addresses must not ship in public source.
    for (const gm of src.match(/[a-z0-9._%+-]+@gmail\.com/gi) || []) {
      // The Privacy Commissioner / example domains are fine; gmail is a person.
      note('identity', `${rel} references a personal gmail address (${gm}) — confirm it is intended to be public`);
    }
    // Unsupported superlatives in customer-facing copy.
    if (f.endsWith('.html')) {
      const superlatives = src.match(/\b(new zealand'?s (?:most )?trusted|nz'?s (?:most )?trusted|#1|number one|official (?:reseller|partner|dealer)|authorised (?:reseller|dealer)|certified (?:reseller|partner))\b/gi);
      if (superlatives) fail('claims', `${rel} uses an unsupported superlative/authority claim: ${[...new Set(superlatives)].join(', ')}`);
    }
  }
}

function auditDispatchCutoff() {
  const files = [...walk(SITE, '.html'), ...walk(path.join(SITE, 'js'), '.js')];
  const CUTOFF = '2pm';
  for (const f of files) {
    const src = read(f);
    if (!src) continue;
    const rel = path.relative(ROOT, f);
    // A rendered "12pm ... dispatch/cut-off" would contradict the 2pm cut-off.
    const bad = src.match(/12\s?pm[^\n]{0,40}(dispatch|cut[-\s]?off|same[-\s]day)/i)
             || src.match(/(dispatch|cut[-\s]?off|same[-\s]day)[^\n]{0,40}12\s?pm/i);
    if (bad) fail('shipping', `${rel} advertises a 12pm cut-off — the site standard is ${CUTOFF}`);
  }
}

function auditAdminLink() {
  for (const f of walk(SITE, '.html')) {
    const src = read(f);
    if (!src) continue;
    const rel = path.relative(ROOT, f);
    const header = src.slice(src.indexOf('<header'), src.indexOf('</header>') + 9);
    if (/id="header-admin-link"|href="\/admin"/.test(header)) {
      fail('admin-link', `${rel} exposes a static Admin link (href="/admin") in header source — must be JS-injected for verified admins only`);
    }
  }
}

function auditGenuineVsCompatible() {
  const page = path.join(SITE, 'html', 'genuine-vs-compatible.html');
  if (!fs.existsSync(page)) { fail('gvc', 'html/genuine-vs-compatible.html does not exist'); return; }
  const src = read(page);
  if (!/<h1[^>]*>[^<]*Genuine[^<]*Compatible/i.test(src)) fail('gvc', 'genuine-vs-compatible page has no Genuine vs Compatible <h1>');
  if (!/not (?:made|licensed|endorsed)/i.test(src)) note('gvc', 'genuine-vs-compatible page should state compatible items are not endorsed by the OEM');

  const vercel = read(path.join(SITE, 'vercel.json')) || '';
  let cfg = {};
  try { cfg = JSON.parse(vercel); } catch { fail('gvc', 'vercel.json does not parse'); }
  const redirect = (cfg.redirects || []).find(r => r.source === '/genuine-vs-compatible');
  if (redirect) fail('gvc', `/genuine-vs-compatible still redirects to ${redirect.destination} — remove it`);
  const rewrite = (cfg.rewrites || []).find(r => r.source === '/genuine-vs-compatible');
  if (!rewrite) fail('gvc', '/genuine-vs-compatible has no rewrite to the real page');

  const footer = read(path.join(SITE, 'js', 'footer.js')) || '';
  if (!footer.includes('/genuine-vs-compatible')) fail('gvc', 'footer.js does not link to /genuine-vs-compatible');
}

function auditShopEmptyState() {
  const shop = read(path.join(SITE, 'html', 'shop.html')) || '';
  // The empty/error copy is allowed to EXIST (it is a real recoverable state),
  // but it must be gated behind a hidden container, never the default render.
  const hasEmpty = /No products found/i.test(shop);
  if (hasEmpty && !/id="empty-state"|class="[^"]*empty[^"]*"[^>]*hidden|hidden[^>]*empty/i.test(shop)) {
    note('shop', 'shop.html contains "No products found" — verify it ships hidden and only shows after a real failure (bots receive backend prerender via middleware.js)');
  }
}

// ──────────────────────────────────────────────────────────────────────────
// LIVE PASS (best-effort)
// ──────────────────────────────────────────────────────────────────────────

function tag(xmlItem, name) {
  const m = xmlItem.match(new RegExp(`<${name}[^>]*>([\\s\\S]*?)</${name}>`, 'i'));
  if (!m) return '';
  return m[1].replace(/<!\[CDATA\[|\]\]>/g, '').trim();
}

function looksCompatible(title /*, desc */) {
  // Key off the TITLE only. A genuine product's description often says
  // "compatible with <printer>", which is legitimate and must not be treated
  // as a third-party product. The GMC title/brand/identifier rules are what
  // matter here, so the title is the correct signal.
  return /\b(compatible|third[-\s]?party|generic|remanufactured|refilled)\b/i.test(title);
}

function auditFeedItem(item) {
  const issues = [];
  const id = tag(item, 'g:id') || tag(item, 'id') || '(no id)';
  const title = tag(item, 'g:title') || tag(item, 'title');
  const desc = tag(item, 'g:description') || tag(item, 'description');
  const brand = tag(item, 'g:brand');
  const mpn = tag(item, 'g:mpn');
  const gtin = tag(item, 'g:gtin');
  const idExists = (tag(item, 'g:identifier_exists') || '').toLowerCase();
  const availability = (tag(item, 'g:availability') || '').toLowerCase();
  const condition = (tag(item, 'g:condition') || '').toLowerCase();
  const price = tag(item, 'g:price');
  const link = tag(item, 'g:link') || tag(item, 'link');

  const isCompatible = looksCompatible(title);

  if (isCompatible) {
    if (!COMPATIBLE_LEADS.test(title)) issues.push('compatible title should START with a non-OEM term (Compatible/Third-party/Generic)');
    if (/\b(genuine|original|oem|manufacturer warranty)\b/i.test(title)) issues.push('compatible title contains an OEM claim (genuine/original/OEM/manufacturer warranty)');
    if (/\b(original|genuine|oem|manufacturer warranty)\b/i.test(desc) && !/not (?:an? )?(?:oem|original|genuine)/i.test(desc)) {
      issues.push('compatible description asserts original/genuine/OEM/manufacturer warranty');
    }
    if (OEM_BRANDS.some(b => new RegExp(`^${b}\\b`, 'i').test(brand))) {
      issues.push(`compatible product uses an OEM brand "${brand}" as g:brand — use the real third-party brand or set identifier_exists=false`);
    }
    if ((gtin || (mpn && OEM_BRANDS.some(b => new RegExp(b, 'i').test(brand)))) && idExists !== 'false') {
      issues.push('compatible product submits OEM-style identifiers without identifier_exists=false');
    }
  }

  if (!title) issues.push('missing title');
  if (!desc) issues.push('missing description');
  if (!link || !/^https?:\/\/[^ ]+\/[^ ]/.test(link)) issues.push('link is missing or not a specific product URL');
  if (!price || !/\d/.test(price)) issues.push('missing price');
  else if (!/NZD/i.test(price)) issues.push(`price "${price}" is not in NZD`);
  if (!['in_stock', 'out_of_stock', 'preorder', 'backorder'].includes(availability)) issues.push(`availability "${availability}" is not a valid GMC value`);
  if (!['new', 'refurbished', 'used'].includes(condition)) issues.push(`condition "${condition}" is not a valid GMC value`);

  // Corrupted text / impossible data.
  const text = `${title} ${desc}`;
  if (/\b(\w+)\s+\1\b/i.test(`${brand} ${brand}`) === false) { /* noop */ }
  for (const b of OEM_BRANDS) {
    if (new RegExp(`\\b${b}\\s+${b}\\b`, 'i').test(text)) issues.push(`duplicated brand token "${b} ${b}"`);
  }
  if (/\b\d+%\b/.test(text) && /compatible|ribbon/i.test(text)) issues.push('coverage token like "5%" leaked into title/description');
  if (/\(\s*\d+\s*pages?\b|\bpages\b\s*\)/i.test(text)) issues.push('page-yield fragment leaked into title/description');
  const yieldMatch = text.match(/([\d,]{2,})\s*pages?\b/i);
  if (yieldMatch) {
    const y = parseInt(yieldMatch[1].replace(/,/g, ''), 10);
    if (Number.isFinite(y) && (y < MIN_PLAUSIBLE_YIELD || y > MAX_PLAUSIBLE_YIELD)) {
      issues.push(`implausible page yield "${yieldMatch[1]}"`);
    }
  }
  if (/ribbon/i.test(title) && /\b\d+\s*pages?\b/i.test(text)) issues.push('ribbon product advertises a page-yield (ribbons are not page-rated)');

  return { id, issues };
}

async function livePass() {
  let feedXml = null;
  try {
    const res = await fetch(FEED_URL, { headers: { 'user-agent': 'mc-readiness-audit' } });
    if (res.ok) feedXml = await res.text();
    else note('feed', `feed fetch returned HTTP ${res.status} — skipping live feed audit`);
  } catch (e) {
    note('feed', `could not fetch feed (${e.message}) — skipping live feed audit (run with network access to enable)`);
  }

  const feedFindings = [];
  if (feedXml) {
    const items = feedXml.match(/<item\b[\s\S]*?<\/item>/gi) || feedXml.match(/<entry\b[\s\S]*?<\/entry>/gi) || [];
    note('feed', `fetched ${items.length} feed items from ${FEED_URL}`);
    for (const it of items) {
      const r = auditFeedItem(it);
      if (r.issues.length) feedFindings.push(r);
    }
    note('feed', `${feedFindings.length}/${items.length} feed items have at least one issue`);
  }

  // Cross-check product API: source vs title.
  try {
    const res = await fetch(API_URL, { headers: { 'user-agent': 'mc-readiness-audit' } });
    if (res.ok) {
      const body = await res.json();
      const products = body.data || body.products || body || [];
      let mismatches = 0;
      for (const p of Array.isArray(products) ? products : []) {
        const title = String(p.name || p.title || '');
        if (p.source === 'compatible' && OEM_TERMS.test(title) && !/compatible/i.test(title)) {
          note('api', `SKU ${p.sku}: source=compatible but title reads OEM ("${title}")`);
          mismatches++;
        }
        if (p.source === 'genuine' && /^compatible\b/i.test(title)) {
          note('api', `SKU ${p.sku}: source=genuine but title starts "Compatible" ("${title}")`);
          mismatches++;
        }
      }
      note('api', `product API cross-check: ${mismatches} source/title mismatch(es) in sample`);
    } else {
      note('api', `product API returned HTTP ${res.status} — skipping cross-check`);
    }
  } catch (e) {
    note('api', `could not fetch product API (${e.message}) — skipping cross-check`);
  }

  return feedFindings;
}

// ──────────────────────────────────────────────────────────────────────────
// RUN
// ──────────────────────────────────────────────────────────────────────────

auditPaymentConsistency();
auditEmailsAndClaims();
auditDispatchCutoff();
auditAdminLink();
auditGenuineVsCompatible();
auditShopEmptyState();

let feedFindings = [];
if (!STATIC_ONLY) feedFindings = await livePass();

if (JSON_OUT) {
  console.log(JSON.stringify({ findings, notes, feedFindings }, null, 2));
} else {
  const line = '─'.repeat(70);
  console.log(`\n${line}\nMerchant Center readiness audit — InkCartridges.co.nz\n${line}`);

  console.log(`\nSTATIC PASS — ${findings.length ? `${findings.length} BLOCKING issue(s)` : 'clean ✓'}`);
  for (const f of findings) console.log(`  ✖ [${f.area}] ${f.msg}`);

  if (notes.length) {
    console.log(`\nNOTES / INFORMATIONAL (${notes.length}):`);
    for (const n of notes) console.log(`  • [${n.area}] ${n.msg}`);
  }

  if (!STATIC_ONLY) {
    if (feedFindings.length) {
      const tally = new Map();
      for (const r of feedFindings) {
        for (const i of r.issues) {
          const key = i.replace(/"[^"]*"/g, '"…"').replace(/\b[A-Z][a-z]+\b(?= as g:brand)/g, '…');
          tally.set(key, (tally.get(key) || 0) + 1);
        }
      }
      console.log('\nLIVE FEED — issue breakdown (count of items):');
      for (const [k, v] of [...tally.entries()].sort((a, b) => b[1] - a[1])) {
        console.log(`  ${String(v).padStart(5)}  ${k}`);
      }
    }
    console.log(`\nLIVE FEED FINDINGS — ${feedFindings.length} item(s) with issues (backend/admin action):`);
    for (const r of feedFindings.slice(0, 100)) {
      console.log(`  SKU ${r.id}:`);
      for (const i of r.issues) console.log(`      - ${i}`);
    }
    if (feedFindings.length > 100) console.log(`  …and ${feedFindings.length - 100} more`);
  }
  console.log(`\n${line}`);
  console.log(findings.length
    ? `RESULT: FAIL — fix ${findings.length} static issue(s) above.`
    : 'RESULT: PASS — static trust/consistency checks clean. Review any live-feed findings with the backend team.');
  console.log(line + '\n');
}

process.exit(findings.length ? 1 : 0);
